public class Table {
    public static void main(String[] args) {
            System.out.println("Table of 2 to 10:- ");
            int a,b;
            for(a = 2; a <= 10; a++){
                System.out.println("Table:- " + a);
                for(b = 1; b <= 10; b++){
                    System.out.println(b*a);
            }
             System.out.println(" ");
        }
    }
}
